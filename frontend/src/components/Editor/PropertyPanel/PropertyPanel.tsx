import React from 'react';
import type { Node } from 'reactflow';
import { getFieldTranslation, getNodeTypeTranslation } from '../../../utils/localization';
import styles from './PropertyPanel.module.css';

interface PropertyPanelProps {
  selectedNode: Node | null;
  onNodeUpdate?: (nodeId: string, data: any) => void;
}

const PropertyPanel: React.FC<PropertyPanelProps> = ({ selectedNode, onNodeUpdate }) => {
  const [nodeData, setNodeData] = React.useState<any>({});

  React.useEffect(() => {
    if (selectedNode) {
      setNodeData(selectedNode.data || {});
    }
  }, [selectedNode]);

  const handleInputChange = (key: string, value: any) => {
    const updatedData = { ...nodeData, [key]: value };
    setNodeData(updatedData);
    
    if (selectedNode && onNodeUpdate) {
      onNodeUpdate(selectedNode.id, updatedData);
    }
  };

  if (!selectedNode) {
    return (
      <div className={styles.propertyPanel}>
        <div className={styles.emptyState}>
          <div className={styles.emptyIcon}>üéØ</div>
          <h3>–í—ã–±–µ—Ä–∏—Ç–µ —É–∑–µ–ª</h3>
          <p>–ö–ª–∏–∫–Ω–∏—Ç–µ –Ω–∞ —É–∑–µ–ª –≤ —Ä–µ–¥–∞–∫—Ç–æ—Ä–µ, —á—Ç–æ–±—ã –Ω–∞—Å—Ç—Ä–æ–∏—Ç—å –µ–≥–æ —Å–≤–æ–π—Å—Ç–≤–∞</p>
        </div>
      </div>
    );
  }

  const getSelectOptions = (key: string) => {
    switch (key) {
      case 'condition':
        return [
          { value: 'contains', label: '—Å–æ–¥–µ—Ä–∂–∏—Ç' },
          { value: 'equals', label: '—Ä–∞–≤–Ω–æ' },
          { value: 'startsWith', label: '–Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å' },
          { value: 'endsWith', label: '–∑–∞–∫–∞–Ω—á–∏–≤–∞–µ—Ç—Å—è –Ω–∞' },
          { value: 'regex', label: '—Ä–µ–≥—É–ª—è—Ä–Ω–æ–µ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ' }
        ];
      case 'parseMode':
        return [
          { value: 'HTML', label: 'HTML' },
          { value: 'Markdown', label: 'Markdown' },
          { value: 'plain', label: '–æ–±—ã—á–Ω—ã–π —Ç–µ–∫—Å—Ç' }
        ];
      case 'messageType':
      case 'triggerType':
        return [
          { value: 'text', label: '—Ç–µ–∫—Å—Ç' },
          { value: 'photo', label: '—Ñ–æ—Ç–æ' },
          { value: 'video', label: '–≤–∏–¥–µ–æ' },
          { value: 'audio', label: '–∞—É–¥–∏–æ' },
          { value: 'document', label: '–¥–æ–∫—É–º–µ–Ω—Ç' }
        ];
      case 'checkType':
        return [
          { value: 'isAdmin', label: '—è–≤–ª—è–µ—Ç—Å—è –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–æ–º' },
          { value: 'isBanned', label: '–∑–∞–±–ª–æ–∫–∏—Ä–æ–≤–∞–Ω' },
          { value: 'hasRole', label: '–∏–º–µ–µ—Ç —Ä–æ–ª—å' }
        ];
      default:
        return null;
    }
  };

  const renderPropertyField = (key: string, value: any) => {
    const selectOptions = getSelectOptions(key);
    
    if (typeof value === 'string') {
      if (selectOptions) {
        return (
          <div key={key} className={styles.field}>
            <label className={styles.label}>
              {getFieldTranslation(key)}
            </label>
            <select
              value={value}
              onChange={(e) => handleInputChange(key, e.target.value)}
              className={styles.select}
            >
              {selectOptions.map(option => (
                <option key={option.value} value={option.value}>
                  {option.label}
                </option>
              ))}
            </select>
          </div>
        );
      }
      
      return (
        <div key={key} className={styles.field}>
          <label className={styles.label}>
            {getFieldTranslation(key)}
          </label>
          <input
            type="text"
            value={value}
            onChange={(e) => handleInputChange(key, e.target.value)}
            className={styles.input}
          />
        </div>
      );
    }

    if (typeof value === 'number') {
      return (
        <div key={key} className={styles.field}>
          <label className={styles.label}>
            {getFieldTranslation(key)}
          </label>
          <input
            type="number"
            value={value}
            onChange={(e) => handleInputChange(key, Number(e.target.value))}
            className={styles.input}
          />
        </div>
      );
    }

    if (typeof value === 'boolean') {
      return (
        <div key={key} className={styles.field}>
          <label className={styles.checkboxLabel}>
            <input
              type="checkbox"
              checked={value}
              onChange={(e) => handleInputChange(key, e.target.checked)}
              className={styles.checkbox}
            />
            {getFieldTranslation(key)}
          </label>
        </div>
      );
    }

    if (Array.isArray(value)) {
      return (
        <div key={key} className={styles.field}>
          <label className={styles.label}>
            {getFieldTranslation(key)}
          </label>
          <textarea
            value={value.join('\n')}
            onChange={(e) => handleInputChange(key, e.target.value.split('\n').filter(Boolean))}
            className={styles.textarea}
            rows={3}
          />
        </div>
      );
    }

    return null;
  };

  return (
    <div className={styles.propertyPanel}>
      <div className={styles.header}>
        <h3>–°–≤–æ–π—Å—Ç–≤–∞ —É–∑–ª–∞</h3>
        <div className={styles.nodeType}>{getNodeTypeTranslation(selectedNode.type || '')}</div>
      </div>

      <div className={styles.content}>
        <div className={styles.section}>
          <div className={styles.field}>
            <label className={styles.label}>–ù–∞–∑–≤–∞–Ω–∏–µ</label>
            <input
              type="text"
              value={selectedNode.data?.label || ''}
              onChange={(e) => handleInputChange('label', e.target.value)}
              className={styles.input}
              placeholder="–í–≤–µ–¥–∏—Ç–µ –Ω–∞–∑–≤–∞–Ω–∏–µ —É–∑–ª–∞"
            />
          </div>
        </div>

        {Object.keys(nodeData).length > 0 && (
          <div className={styles.section}>
            <h4>–ù–∞—Å—Ç—Ä–æ–π–∫–∏</h4>
            {Object.entries(nodeData).map(([key, value]) => {
              // –°–∫—Ä—ã–≤–∞–µ–º —Ç–µ—Ö–Ω–∏—á–µ—Å–∫–∏–µ –ø–æ–ª—è
              if (['label', 'id', 'type', 'category', 'color', 'icon', 'inputs', 'outputs', 'name', 'description', 'tags', 'compatibility'].includes(key.toLowerCase())) {
                return null;
              }
              return renderPropertyField(key, value);
            })}
          </div>
        )}
      </div>
    </div>
  );
};

export default PropertyPanel;